import Foundation
import SwiftUI

extension Font {
    static func verdanaBold(size: CGFloat) -> Font {
        return Font.custom("Verdana-Bold", size: size)
    }
    
    static func verdana(size: CGFloat) -> Font {
        return Font.custom("Verdana", size: size)
    }
}
